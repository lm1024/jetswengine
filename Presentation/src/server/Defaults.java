package server;

public class Defaults {
	
	private String backgroundColour;
	private Image backgroundImage;
	private String font;
	private int fontSize;
	private String fontColour;
	private String lineColour;
	private String fillColour;
	/**
	 * @return the backgroundColour
	 */
	public String getBackgroundColour() {
		return backgroundColour;
	}
	/**
	 * @param backgroundColour the backgroundColour to set
	 */
	public void setBackgroundColour(String backgroundColour) {
		this.backgroundColour = backgroundColour;
	}
	/**
	 * @return the backgroundImage
	 */
	public Image getBackgroundImage() {
		return backgroundImage;
	}
	/**
	 * @param backgroundImage the backgroundImage to set
	 */
	public void setBackgroundImage(Image backgroundImage) {
		this.backgroundImage = backgroundImage;
	}
	/**
	 * @return the font
	 */
	public String getFont() {
		return font;
	}
	/**
	 * @param font the font to set
	 */
	public void setFont(String font) {
		this.font = font;
	}
	/**
	 * @return the fontSize
	 */
	public int getFontSize() {
		return fontSize;
	}
	/**
	 * @param fontSize the fontSize to set
	 */
	public void setFontSize(int fontSize) {
		this.fontSize = fontSize;
	}
	/**
	 * @return the fontColour
	 */
	public String getFontColour() {
		return fontColour;
	}
	/**
	 * @param fontColour the fontColour to set
	 */
	public void setFontColour(String fontColour) {
		this.fontColour = fontColour;
	}
	/**
	 * @return the lineColour
	 */
	public String getLineColour() {
		return lineColour;
	}
	/**
	 * @param lineColour the lineColour to set
	 */
	public void setLineColour(String lineColour) {
		this.lineColour = lineColour;
	}
	/**
	 * @return the fillColour
	 */
	public String getFillColour() {
		return fillColour;
	}
	/**
	 * @param fillColour the fillColour to set
	 */
	public void setFillColour(String fillColour) {
		this.fillColour = fillColour;
	}
	
}
